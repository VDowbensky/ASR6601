void SSD1306_Display_On(void)
{
	SSD1306_WR_Byte(0X8D,SSD1306_CMD);  //SET DCDC Command
	SSD1306_WR_Byte(0X14,SSD1306_CMD);  //DCDC ON
	SSD1306_WR_Byte(0XAF,SSD1306_CMD);  //DISPLAY ON
}

void SSD1306_Display_Off(void)
{
	SSD1306_WR_Byte(0X8D,SSD1306_CMD);  //SET DCDC Command
	SSD1306_WR_Byte(0X10,SSD1306_CMD);  //DCDC OFF
	SSD1306_WR_Byte(0XAE,SSD1306_CMD);  //DISPLAY OFF
}

void SSD1306_Set_Pos(uint8_t x, uint8_t y)
{
 	SSD1306_WR_Byte(YLevel+y/PAGE_SIZE,SSD1306_CMD);
	SSD1306_WR_Byte((((x+2)&0xf0)>>4)|0x10,SSD1306_CMD);
	SSD1306_WR_Byte(((x+2)&0x0f),SSD1306_CMD); 
}
  		    
void SSD1306_Init(void)
{
 	SSD1306_interface_init();
 	delay_ms(10);
	SSD1306_Reset();

/**************Init SSD1306*****************/	
	SSD1306_WR_Byte(0xAE,SSD1306_CMD); /*display off*/
	SSD1306_WR_Byte(0x00,SSD1306_CMD); /*set lower column address*/
	SSD1306_WR_Byte(0x10,SSD1306_CMD); /*set higher column address*/
	SSD1306_WR_Byte(0x40,SSD1306_CMD); /*set display start line*/ 
	SSD1306_WR_Byte(0xB0,SSD1306_CMD); /*set page address*/
	SSD1306_WR_Byte(0x81,SSD1306_CMD); /*contract control*/ 
	SSD1306_WR_Byte(0xff,SSD1306_CMD); /*128*/
	SSD1306_WR_Byte(0xA1,SSD1306_CMD); /*set segment remap*/ 
	SSD1306_WR_Byte(0xA6,SSD1306_CMD); /*normal / reverse*/
	SSD1306_WR_Byte(0xA8,SSD1306_CMD); /*multiplex ratio*/ 
	SSD1306_WR_Byte(0x3F,SSD1306_CMD); /*duty = 1/64*/
	SSD1306_WR_Byte(0xC8,SSD1306_CMD); /*Com scan direction*/
	SSD1306_WR_Byte(0xD3,SSD1306_CMD); /*set display offset*/ 
	SSD1306_WR_Byte(0x00,SSD1306_CMD);
	SSD1306_WR_Byte(0xD5,SSD1306_CMD); /*set osc division*/ 
	SSD1306_WR_Byte(0x80,SSD1306_CMD);
	SSD1306_WR_Byte(0xD9,SSD1306_CMD); /*set pre-charge period*/ 
	SSD1306_WR_Byte(0XF1,SSD1306_CMD);
	SSD1306_WR_Byte(0xDA,SSD1306_CMD); /*set COM pins*/ 
	SSD1306_WR_Byte(0x12,SSD1306_CMD);
	SSD1306_WR_Byte(0xDB,SSD1306_CMD); /*set vcomh*/ 
	SSD1306_WR_Byte(0x30,SSD1306_CMD);
	SSD1306_WR_Byte(0x8D,SSD1306_CMD); /*set charge pump disable*/ 
	SSD1306_WR_Byte(0x14,SSD1306_CMD);
	SSD1306_WR_Byte(0xAF,SSD1306_CMD); /*display ON*/
}

void SSD1306_Set_Pixel(uint8_t x, uint8_t y,uint8_t color)
{
	if(color) SSD1306_buffer[(y/PAGE_SIZE)*WIDTH+x]|= (1<<(y%PAGE_SIZE))&0xff;
	else SSD1306_buffer[(y/PAGE_SIZE)*WIDTH+x]&= ~((1<<(y%PAGE_SIZE))&0xff);
}

void SSD1306_Display(void)
{
	uint8_t i,n;		    
	for(i=0;i<PAGE_SIZE;i++)  
	{  
		SSD1306_WR_Byte (YLevel+i,SSD1306_CMD);    //Set page address (0~7)
		SSD1306_WR_Byte (XLevelL,SSD1306_CMD);     //Set the display position - column low address
		SSD1306_WR_Byte (XLevelH,SSD1306_CMD);     //Set display position - column high address  
		//Update Display
		for(n=0;n<WIDTH;n++) SSD1306_WR_Byte(SSD1306_buffer[i*WIDTH+n],SSD1306_DATA); 
	}   
}

void SSD1306_Clear(uint8_t dat)
{
	if(dat) memset(SSD1306_buffer,0xff,sizeof(SSD1306_buffer));
	else memset(SSD1306_buffer,0,sizeof(SSD1306_buffer));
	SSD1306_Display();
}
